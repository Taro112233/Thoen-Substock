function extractExportedFunction(content, filename) {
  const functionMatch = content.match(/export async function (\w+)\([^)]*\)\s*\{([\s\S]*)\}/);
  
  if (!functionMatch) {
    console.warn(`âš ï¸  No exported function found in ${filename}`);
    return null;
  }

  const functionName = functionMatch[1];
  
  return {
    name: functionName,
    sourceFile: filename
  };
}

function mergeSeeds() {
  console.log('ğŸŒ± Hospital Pharmacy Seed Merger v3.0 - Complete Requisition System');
  console.log('====================================================================');
  
  if (!fs.existsSync(SEEDS_DIR)) {
    console.error(`âŒ Seeds directory not found: ${SEEDS_DIR}`);
    process.exit(1);
  }

  const seedFiles = fs.readdirSync(SEEDS_DIR)
    .filter(file => file.endsWith('.seed.ts'))
    .sort((a, b) => {
      const orderA = SEED_ORDER[a] ?? 999;
      const orderB = SEED_ORDER[b] ?? 999;
      return orderA - orderB;
    });

  if (seedFiles.length === 0) {
    console.error('âŒ No .seed.ts files found in seeds directory');
    process.exit(1);
  }

  console.log(`ğŸ“ Found ${seedFiles.length} seed files:`);
  seedFiles.forEach((file, index) => {
    const order = SEED_ORDER[file] ?? 999;
    console.log(`  ${order + 1}. ${file}`);
  });

  const extractedFunctions = [];
  const seenFunctions = new Set();

  // Extract functions from each seed file
  for (const file of seedFiles) {
    const filePath = path.join(SEEDS_DIR, file);
    const content = fs.readFileSync(filePath, 'utf8');
    
    console.log(`ğŸ“– Processing ${file}...`);
    
    const extracted = extractExportedFunction(content, file);
    if (extracted) {
      // à¸›à¹‰à¸­à¸‡à¸à¸±à¸™ function à¸‹à¹‰à¸³ - à¹ƒà¸«à¹‰ priority à¸•à¸²à¸¡ file order
      if (seenFunctions.has(extracted.name)) {
        console.log(`  âš ï¸  Skipping duplicate function: ${extracted.name} from ${file}`);
        continue;
      }
      
      extractedFunctions.push(extracted);
      seenFunctions.add(extracted.name);
      console.log(`  âœ… Extracted function: ${extracted.name} from ${file}`);
    }
  }

  // à¸•à¸£à¸§à¸ˆà¸ªà¸­à¸šà¸§à¹ˆà¸²à¸¡à¸µ function à¸—à¸µà¹ˆà¸ˆà¸³à¹€à¸›à¹‡à¸™à¸«à¸£à¸·à¸­à¹„à¸¡à¹ˆ
  const hasWarehouseFunction = extractedFunctions.some(f => f.name === 'seedWarehouses');
  const hasRequisitionFunction = extractedFunctions.some(f => f.name === 'seedRequisitionsAndTransactions');
  const drugFunctions = extractedFunctions.filter(f => 
    f.name === 'seedRealDrugs' || f.name === 'seedBulkRealDrugs'
  );

  if (drugFunctions.length === 0) {
    console.error('âŒ No drug seeding function found!');
    process.exit(1);
  }

  const selectedDrugFunction = drugFunctions[0];
  console.log(`\nğŸ¯ Using drug function: ${selectedDrugFunction.name} from ${selectedDrugFunction.sourceFile}`);
  
  if (hasWarehouseFunction) {
    console.log(`ğŸª Warehouse creation: âœ… Enabled`);
  } else {
    console.log(`ğŸª Warehouse creation: âš ï¸  Missing - will be auto-created`);
  }

  if (hasRequisitionFunction) {
    console.log(`ğŸ“‹ Requisition system: âœ… Enabled`);
  } else {
    console.log(`ğŸ“‹ Requisition system: âš ï¸  Missing - basic system will be created`);
  }

  // Generate merged seed file
  const mergedContent = generateMergedSeed(extractedFunctions, selectedDrugFunction, hasWarehouseFunction, hasRequisitionFunction);
  
  // Write merged file
  fs.writeFileSync(OUTPUT_FILE, mergedContent, 'utf8');
  
  console.log(`\nâœ… Successfully merged ${extractedFunctions.length} unique seed functions`);
  console.log(`ğŸ“¦ Generated seed file with complete hospital pharmacy system`);
}

