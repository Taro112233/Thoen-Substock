function getFileNameFromFunction(functionName) {
  const mapping = {
    'seedHospitals': 'hospitals.seed',
    'seedPersonnelTypes': 'personnel-types.seed',
    'seedDepartments': 'departments.seed',
    'seedUsers': 'users.seed',
    'seedMasterData': 'master-data.seed',
    'seedRealDrugs': 'real-drugs.seed', // Add real drugs mapping
    'seedDemoData': 'demo-data.seed'
  };
  
  return mapping[functionName] || `${functionName.toLowerCase()}.seed`;
}

function validateMerge() {
  console.log('üîç Validating merged seed file...');
  
  if (!fs.existsSync(OUTPUT_FILE)) {
    console.error('‚ùå Merged seed file not found');
    return false;
  }
  
  const content = fs.readFileSync(OUTPUT_FILE, 'utf8');
  
  // Check for required imports
  const requiredImports = ['PrismaClient', 'hashPassword'];
  const missingImports = requiredImports.filter(imp => !content.includes(imp));
  
  if (missingImports.length > 0) {
    console.error(`‚ùå Missing required imports: ${missingImports.join(', ')}`);
    return false;
  }
  
  // Check for required function calls
  const requiredFunctions = ['seedHospitals', 'seedPersonnelTypes', 'seedDepartments', 'seedUsers', 'seedMasterData', 'seedRealDrugs'];
  const missingFunctions = requiredFunctions.filter(func => !content.includes(`await ${func}(`));
  
  if (missingFunctions.length > 0) {
    console.error(`‚ùå Missing function calls: ${missingFunctions.join(', ')}`);
    return false;
  }
  
  console.log('‚úÖ Merged seed file validation passed');
  return true;
}

